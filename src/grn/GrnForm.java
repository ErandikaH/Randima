    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(GrnForm.this, "non selected row");
        }
        if (selectedRow != -1) {

            String id = (String) jTable1.getValueAt(selectedRow, 0);

            try {

                Grn grn = GrnUtilities.searchGrn(id);

                if (grn != null) {
                    idText.setText(grn.getGrnId());
                    supIdText.setText(grn.getSupId());
                    dateText.setText(grn.getGrnDate() + "");
                    amountText.setText(grn.getFullAmount() + "");

                } else {
                    JOptionPane.showMessageDialog(GrnForm.this, "No Such GRN :" + id);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(GrnForm.this, "Wrong sql");
            }
        }
        
        validator.removeWarning(idText);
        validator.removeWarning(dateText);
        validator.removeWarning(supIdText);
        validator.removeWarning(amountText);
    }//GEN-LAST:event_jTable1MouseClicked

