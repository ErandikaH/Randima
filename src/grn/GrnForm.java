    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        String id = idText.getText();
        double amount = 0 ;
        String supId = supIdText.getText();
        String grnDate = dateText.getText();
        DateFormat formatter;
        java.util.Date date = null;
        formatter = new SimpleDateFormat("yyyy-MM-dd");
        try {
            java.util.Date dt = formatter.parse(grnDate);
            date = new java.sql.Date(dt.getTime());

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Wrong Date...");
            // ex.printStackTrace();
        }

        try {
            amount = Double.parseDouble(amountText.getText());
        } catch (NumberFormatException nm) {
            JOptionPane.showMessageDialog(this, "Invalid amount..");
        }

        Grn grn = new Grn(id, supId, date, amount);
        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int res = JOptionPane.showOptionDialog(null, "Do you want to save the changes to GRN?", "Update GRN", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (res == JOptionPane.YES_OPTION) {
                if (GrnUtilities.addsearchGrn(id) != null) {
                    if (ValidationGRN.isValidGrnId(id) && ValidationGRN.isValidSupId(supId) && ValidationGRN.isValidDate(grnDate) && ValidationGRN.isValidAmount(amountText.getText())) {

                        validator.removeWarning(idText);
                        validator.removeWarning(supIdText);
                        validator.removeWarning(dateText);
                        validator.removeWarning(amountText);


                        int rows = GrnUtilities.updateGrn(grn);
                        if (rows > 0) {
                            JOptionPane.showMessageDialog(GrnForm.this, "Update successfully");
                        } else {
                            JOptionPane.showMessageDialog(GrnForm.this, "No such GRN : " + id);
                        }

                    } else if (!ValidationGRN.isValidGrnId(id)) {
                        validator.addWarning(idText);
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret GRN ID...");
                    } else if (!ValidationGRN.isValidSupId(supId)) {
                        validator.addWarning(supIdText);
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Supplier ID...");
                    } else if (!ValidationGRN.isValidDate(grnDate)) {
                        validator.addWarning(dateText);
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Date...");
                    } else if (!ValidationGRN.isValidAmount(amountText.getText())) {
                        validator.addWarning(amountText);
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Amount...");
                    } else {
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Data...");
                    }
                } else {
                    validator.addWarning(idText);
                    JOptionPane.showMessageDialog(GrnForm.this, "The GRN ID you have entered is in the Database...");
                }
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(GrnForm.this, "Wrong SQL");
            //e.printStackTrace();
        }

    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        String id = idText.getText();
        Grn grn = new Grn(id);

        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int option = JOptionPane.showOptionDialog(null, "Are you sure you want to permanently delete this GRN?", "Delete GRN", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (option == JOptionPane.YES_OPTION) {

                int rows = GrnUtilities.deleteGrn(grn);
                if (rows > 0) {
                    JOptionPane.showMessageDialog(GrnForm.this, "Deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(GrnForm.this, "No such GRN : " + id);
                }
            }

            if (option == JOptionPane.NO_OPTION) {

                GrnForm.this.setVisible(false);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(GrnForm.this, "Wrong SQL");
            //e.printStackTrace();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

