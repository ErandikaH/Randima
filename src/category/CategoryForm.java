    private void searchTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchTextActionPerformed

        if (searchText.getText().isEmpty()) {
            searchText.setText("Search...");
        }
        String search = searchText.getText();

        try {
            Category category = CategoryUtilities.searchCategory(search);

            if (category != null) {
                codeText.setText(search);
                nameText.setText(category.categoryName);

            } else {
                JOptionPane.showMessageDialog(CategoryForm.this, "No Such Category :" + search);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(CategoryForm.this, "Wrong sql");
        }
    }//GEN-LAST:event_searchTextActionPerformed

