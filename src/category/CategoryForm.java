    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(CategoryForm.this, "non selected row");
        }
        if (selectedRow != -1) {

            String code = (String) jTable1.getValueAt(selectedRow, 0);
            // JOptionPane.showMessageDialog(Form.this, "selected value :" + id);

            try {
                Category category = CategoryUtilities.searchCategory(code);

                if (category != null) {
                    codeText.setText(category.categoryId);
                    nameText.setText(category.categoryName);

                } else {
                    JOptionPane.showMessageDialog(CategoryForm.this, "No Such Category :" + code);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(CategoryForm.this, "Wrong sql");
            }
        }
        
        validator.removeWarning(codeText);
        validator.removeWarning(nameText);
    }//GEN-LAST:event_jTable1MouseClicked

