    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        String code = codeText.getText();
        String name = nameText.getText();

        Category category = new Category(code, name);

        try {
            if (CategoryUtilities.addsearchCategory(code) == null) {
                validator.removeWarning(codeText);
                if (CategoryValidation.isValidCategoryId(code) && CategoryValidation.isValidName(name)) {
                    int rows = CategoryUtilities.addCategory(category);
                    if (rows > 0) {
                        JOptionPane.showMessageDialog(CategoryForm.this, "Successfully Addedd.");
                    }
                } else if (!CategoryValidation.isValidCategoryId(code)) {
                    validator.addWarning(codeText);
                    JOptionPane.showMessageDialog(CategoryForm.this, "The Category ID already entered wrong.");
                } else if (!CategoryValidation.isValidName(name)) {
                    validator.addWarning(nameText);
                    JOptionPane.showMessageDialog(CategoryForm.this, "The Category Name already entered wrong.");
                } else {
                    JOptionPane.showMessageDialog(CategoryForm.this, "The data already entered wrong.");
                }
            } else {
                validator.addWarning(codeText);
                JOptionPane.showMessageDialog(CategoryForm.this, "The Category ID you have entered is in the database.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CategoryForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed

        String code = codeText.getText();
        String name = nameText.getText();

        Category category = new Category(code, name);
        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int res = JOptionPane.showOptionDialog(null, "Do you want to save the changes to Category?", "Update Category", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (res == JOptionPane.YES_OPTION) {

                if (CategoryUtilities.addsearchCategory(code) != null) {
                    validator.removeWarning(codeText);
                    if (CategoryValidation.isValidCategoryId(code) && CategoryValidation.isValidName(name)) {
                        int rows = CategoryUtilities.updateCategory(category);
                        if (rows > 0) {
                            JOptionPane.showMessageDialog(CategoryForm.this, "Update successfully");
                        } else {
                            JOptionPane.showMessageDialog(CategoryForm.this, "No such Category : " + code);
                        }
                    } else if (!CategoryValidation.isValidCategoryId(code)) {
                        validator.addWarning(codeText);
                        JOptionPane.showMessageDialog(CategoryForm.this, "The Category ID already entered wrong.");
                    } else if (!CategoryValidation.isValidName(name)) {
                        validator.addWarning(nameText);
                        JOptionPane.showMessageDialog(CategoryForm.this, "The Category Name already entered wrong.");
                    } else {
                        JOptionPane.showMessageDialog(CategoryForm.this, "The data already entered wrong.");
                    }
                } else {
                    validator.addWarning(codeText);
                    JOptionPane.showMessageDialog(CategoryForm.this, "The Category ID you have entered is not in the database.");
                }
            }
            if (res == JOptionPane.NO_OPTION) {
                CategoryForm.this.setVisible(false);

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(CategoryForm.this, "Wrong SQL");
            //e.printStackTrace();
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        String code = codeText.getText();
        Category category = new Category(code);

        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int option = JOptionPane.showOptionDialog(null, "Are you sure you want to permanently delete this Category?", "Delete Category", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (option == JOptionPane.YES_OPTION) {

                int rows = CategoryUtilities.deleteCategory(category);
                if (rows > 0) {
                    JOptionPane.showMessageDialog(CategoryForm.this, "Deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(CategoryForm.this, "No such Category : " + code);
                }
            }

            if (option == JOptionPane.NO_OPTION) {
                CategoryForm.this.setVisible(false);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(CategoryForm.this, "Wrong SQL");
            //e.printStackTrace();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

