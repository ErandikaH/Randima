    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        String id = idText.getText();
        Order order = new Order(id);

        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int option = JOptionPane.showOptionDialog(null, "Are you sure you want to permanently delete this Order?", "Delete Order", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (option == JOptionPane.YES_OPTION) {

                int rows = OrderUtilities.deleteOrder(order);
                if (rows > 0) {
                    JOptionPane.showMessageDialog(OrderForm.this, "Deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(OrderForm.this, "No such Order : " + id);
                }
            }

            if (option == JOptionPane.NO_OPTION) {

                OrderForm.this.setVisible(false);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(OrderForm.this, "Wrong SQL");
            //e.printStackTrace();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

