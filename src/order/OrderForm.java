    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(OrderForm.this, "non selected row");
        }
        if (selectedRow != -1) {

            String id = (String) jTable1.getValueAt(selectedRow, 0);
            // JOptionPane.showMessageDialog(Form.this, "selected value :" + id);

            try {

                Order order = OrderUtilities.searchOrder(id);

                if (order != null) {
                    idText.setText(order.getOrderId());
                    buyerText.setText(order.getBuyerId());
                    orderDateText.setText("" + order.getOrderDate());
                    deliveryDateChooser.setDate(order.getDeliveryDate());
                    statusText.setText(order.getStatus() + "");

                } else {
                    JOptionPane.showMessageDialog(OrderForm.this, "No Such Order :" + id);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(OrderForm.this, "Wrong sql");
            }
        }

        validator.removeWarning(idText);
        validator.removeWarning(buyerText);
        validator.removeWarning(orderDateText);
        validator.removeWarning(deliveryDateChooser);
        validator.removeWarning(statusText);
    }//GEN-LAST:event_jTable1MouseClicked

