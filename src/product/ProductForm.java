    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(ProductForm.this, "non selected row");
        }
        if (selectedRow != -1) {

            String code = (String) jTable1.getValueAt(selectedRow, 0);
            // JOptionPane.showMessageDialog(Form.this, "selected value :" + id);

            try {

                Product product = ProductUtilities.searchProduct(code);

                if (product != null) {
                    codeText.setText(product.getProductCode());
                    descriptionText.setText(product.getProductDescription());
                    gradeText.setText(product.getGrade());
                    priceText.setText("" + product.getProductUnitPrice());
                    weightText.setText("" + product.getUnitWeight());
                    qtyText.setText("" + product.getProductQtyOnHand());
                    reorderText.setText("" + product.getProductReorderLevel());
                    categoryIdText.setText(product.getCategoryId());

                } else {
                    JOptionPane.showMessageDialog(ProductForm.this, "No Such Product :" + code);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(ProductForm.this, "Wrong sql");
            }
        }
        
        validator.removeWarning(codeText);
        validator.removeWarning(descriptionText);
        validator.removeWarning(gradeText);
        validator.removeWarning(priceText);
        validator.removeWarning(weightText);
        validator.removeWarning(qtyText);
        validator.removeWarning(reorderText);
        validator.removeWarning(categoryIdText);
    }//GEN-LAST:event_jTable1MouseClicked

