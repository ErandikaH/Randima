    private void logInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInButtonActionPerformed
        String userName = userNameText.getText();
        String password = new String(passwordText.getPassword());

        if(userName.equals("Erandika") && password.equals("erandika")){
            //JOptionPane.showMessageDialog(this, "LogIn successful");
            LogIn.this.setVisible(false);
            LogIn.this.dispose();
            new LogInStep().setVisible(true);            
        }else{
            JOptionPane.showMessageDialog(this, "LogIn failed...");
        }
    }//GEN-LAST:event_logInButtonActionPerformed

    private void userNameTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userNameTextActionPerformed
        if(userNameText.getText().equals("Erandika")){
            passwordText.requestFocus();
        }
    }//GEN-LAST:event_userNameTextActionPerformed

    private void passwordTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passwordTextActionPerformed
        String password = new String(passwordText.getPassword());
        if(password.equals("erandika")){
            logInButton.requestFocus();
        }
    }//GEN-LAST:event_passwordTextActionPerformed

    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        int res = JOptionPane.showConfirmDialog(this, "Do you want to Exit ?");
        if (res == JOptionPane.YES_OPTION) {
            System.exit(0);
        }
    }//GEN-LAST:event_cancelButtonActionPerformed

