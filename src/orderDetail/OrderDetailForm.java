    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(OrderDetailForm.this, "non selected row");
        }
        if (selectedRow != -1) {

            String id = (String) jTable1.getValueAt(selectedRow, 0);
            String code = (String) jTable1.getValueAt(selectedRow, 1);
            // JOptionPane.showMessageDialog(Form.this, "selected value :" + id);

            try {

                OrderDetail orderDetail = OrderDetailUtilities.searchOrderDetail(id, code);

                if (orderDetail != null) {
                    orderIdText.setText(orderDetail.getOrderId());
                    codeText.setText(orderDetail.getProductCode());
                    discountText.setText("" + orderDetail.getDiscount());
                    unitPriceText.setText("" + orderDetail.getUnitPrice());
                    containerNoText.setText(orderDetail.getContainerNo());
                    qtyText.setText("" + orderDetail.getQty());

                } else {
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "No Such Product :" + id + " & " + code);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(OrderDetailForm.this, "Wrong sql");
            }
        }
        
        validator.removeWarning(orderIdText);
        validator.removeWarning(codeText);
        validator.removeWarning(qtyText);
        validator.removeWarning(unitPriceText);
        validator.removeWarning(containerNoText);
        validator.removeWarning(discountText);
    }//GEN-LAST:event_jTable1MouseClicked

