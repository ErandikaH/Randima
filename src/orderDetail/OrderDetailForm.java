    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        String code = codeText.getText();
        String orderId = orderIdText.getText();
        double price = 0;
        double discount = 0;
        String containerNo = containerNoText.getText();
        int qty = 0;

        try {

            price = Double.parseDouble(unitPriceText.getText());
            discount = Double.parseDouble(discountText.getText());
            qty = Integer.parseInt(qtyText.getText());
        } catch (NumberFormatException nm) {
            //JOptionPane.showMessageDialog(this, "Wrong Input...");
        }

        OrderDetail orderDetail = new OrderDetail(orderId, code, qty, discount, containerNo, price);

        try {
            if (OrderDetailUtilities.addsearchOrderDetail(orderId, code) == null) {
                validator.removeWarning(orderIdText);

                if (ValidationOrderDetail.isValidOrderId(orderId) && ValidationOrderDetail.isValidProductCode(code) && ValidationOrderDetail.isValidContainerNo(containerNo) && ValidationOrderDetail.isValidPrice(unitPriceText.getText()) && ValidationOrderDetail.isValidQty(qtyText.getText()) && ValidationOrderDetail.isValidDiscount(discountText.getText())) {
                    int rows = OrderDetailUtilities.addOrderDetail(orderDetail);
                    if (rows > 0) {
                        JOptionPane.showMessageDialog(OrderDetailForm.this, "Successfully Addedd.");
                    }

                } else if (!ValidationOrderDetail.isValidOrderId(orderId)) {
                    validator.addWarning(orderIdText);
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "The Order ID already entered wrong.");
                } else if (!ValidationOrderDetail.isValidProductCode(code)) {
                    validator.addWarning(codeText);
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "The Product Code already entered wrong.");
                } else if (!ValidationOrderDetail.isValidPrice(unitPriceText.getText())) {
                    validator.addWarning(unitPriceText);
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "The Price already entered wrong.");
                } else if (!ValidationOrderDetail.isValidQty(qtyText.getText())) {
                    validator.addWarning(qtyText);
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "The Qty already entered wrong.");
                } else if (!ValidationOrderDetail.isValidDiscount(discountText.getText())) {
                    validator.addWarning(discountText);
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "The Discount already entered wrong.");
                } else if (!ValidationOrderDetail.isValidContainerNo(containerNo)) {
                    validator.addWarning(containerNoText);
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "The Container No already entered wrong.");
                } else {
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "Wrong Input...");
                }
            } else {
                validator.addWarning(orderIdText);
                JOptionPane.showMessageDialog(OrderDetailForm.this, "The Order ID & Product Code you have entered is in the database.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(OrderDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

