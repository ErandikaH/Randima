    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        String code = codeText.getText();
        String orderId = orderIdText.getText();

        OrderDetail orderDetail = new OrderDetail(orderId, code);

        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int option = JOptionPane.showOptionDialog(null, "Are you sure you want to permanently delete this Order Detail?", "Delete Order Detail", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (option == JOptionPane.YES_OPTION) {

                int rows = OrderDetailUtilities.deleteOrderDetail(orderDetail);
                if (rows > 0) {
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "Deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(OrderDetailForm.this, "No such Order Detail : " + orderId + " & " + code);
                }
            }

            if (option == JOptionPane.NO_OPTION) {

                OrderDetailForm.this.setVisible(false);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(OrderDetailForm.this, "Wrong SQL");
            //e.printStackTrace();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

