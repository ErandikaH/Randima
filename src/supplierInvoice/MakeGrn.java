        String grnDate = dateLabel.getText();
        DateFormat formatter;
        java.util.Date date = null;
        formatter = new SimpleDateFormat("yyyy-MM-dd");
        try {
            java.util.Date dt = formatter.parse(grnDate);
            date = new java.sql.Date(dt.getTime());

        } catch (ParseException ex) {
            //ex.printStackTrace();
        }

        try {
            amount = Double.parseDouble(fullAmountText.getText());
        } catch (NumberFormatException nm) {
        }
        Grn grn = new Grn(id, supId, date, amount);

        try {
            Object[] options = {"Yes", "No", "Cancel"};
            int res = JOptionPane.showOptionDialog(null, "Do you want to save the changes to GRN?", "Add GRN", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (res == JOptionPane.YES_OPTION) {
                if (GrnUtilities.addsearchGrn(id) == null) {
                    if (ValidationGRN.isValidGrnId(id) && ValidationGRN.isValidSupId(supId) && ValidationGRN.isValidDate(grnDate) && ValidationGRN.isValidAmount(fullAmountText.getText())) {

                        int rows = GrnUtilities.addGrn(grn);
                        if (rows > 0) {
                            JOptionPane.showMessageDialog(MakeGrn.this, "Successfully Addedd.");
                            payButton.setEnabled(true);
                        }

                    } else if (!ValidationGRN.isValidGrnId(id)) {
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret GRN ID...");
                    } else if (!ValidationGRN.isValidSupId(supId)) {
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Supplier ID...");
                    } else if (!ValidationGRN.isValidDate(grnDate)) {
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Date...");
                    } else if (!ValidationGRN.isValidAmount(fullAmountText.getText())) {
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Amount...");
                    } else {
                        JOptionPane.showMessageDialog(this, "Please Enter the Corret Data...");
                    }
                } else {
                    JOptionPane.showMessageDialog(MakeGrn.this, "The GRN ID you have entered is in the Database...");
                }

            }


        } catch (SQLException ex) {
            Logger.getLogger(GrnForm.class.getName()).log(Level.SEVERE, null, ex);
        }


        //update Item

        int qty = 0;
        String description = (String) (itemComboBox.getSelectedItem());

        try {
            qty = newQtyOnHand;

        } catch (NumberFormatException nm) {
        }

        Item item = new Item(description, qty);

        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int res = JOptionPane.showOptionDialog(null, "Do you want to save the changes to Item?", "Update Item", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (res == JOptionPane.YES_OPTION) {

                if (ValidationItem.isValidDescription(description) && ValidationItem.isValidQty(String.valueOf(newQtyOnHand))) {

                    int rows = ItemUtilities.updateItem(description, qty);
                    if (rows > 0) {
                        JOptionPane.showMessageDialog(MakeGrn.this, "Update successfully");
                    } else {
                        JOptionPane.showMessageDialog(MakeGrn.this, "No such Item : ");
                    }

                } else {
                    JOptionPane.showMessageDialog(MakeGrn.this, "The data already entered wrong.");
                }

            }


        } catch (SQLException e) {
            JOptionPane.showMessageDialog(MakeGrn.this, "Wrong SQL");
            //e.printStackTrace();
        }

