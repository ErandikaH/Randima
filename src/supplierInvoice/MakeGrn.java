    private void qtyTextKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_qtyTextKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            try {
                qty = Integer.parseInt(qtyText.getText());

                String description = (String) (itemComboBox.getSelectedItem());

                Item i = null;

                try {
                    i = ItemUtilities.searchDescription(description);

                    Object data[] = new Object[6];

                    data[0] = i.getItemcode();
                    data[1] = description;
                    data[2] = qty;
                    data[3] = qty * (double) 3 / 100;
                    data[4] = i.getUnitPrice();
                    data[5] = i.getUnitPrice() * qty * (double) 97 / 100;

                    ((DefaultTableModel) jTable.getModel()).addRow(data);

                    qtyText.setText("");
                    int row_count = jTable.getRowCount();

                    double total = 0.0;

                    for (int k = 0; k < row_count; k++) {
                        Double amount_at_this_row = (Double) (jTable.getValueAt(k, 5));
                        total += amount_at_this_row;
                    }
                    fullAmountText.setText("" + total);

                    newQtyOnHand = qtyOnHand + qty;

                    //qtyOnHandText.setText("" + newQtyOnHand);


                } catch (SQLException ex) {
                    //Logger.getLogger(AddOrderForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (NumberFormatException nm) {
            }

        }
    }//GEN-LAST:event_qtyTextKeyPressed

