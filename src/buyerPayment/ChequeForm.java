    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String search = searchText.getText();

        try {

            Cheque cheque = ChequeUtilities.searchCheque(search);

            if (cheque != null) {
                chequeNoText.setText(search);
                idText.setText(cheque.getPaymentId());
                nameText.setText(cheque.getBankName());
                dateChooser.setDate(cheque.getChequeDate());
                statusText.setText(cheque.isStatus() + "");
                amountText.setText(cheque.getChequeAmount() + "");

            } else {
                JOptionPane.showMessageDialog(ChequeForm.this, "No Such Cheque :" + search);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(ChequeForm.this, "Wrong sql");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

