    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(ChequeForm.this, "non selected row");
        }
        if (selectedRow != -1) {

            String id = (String) jTable1.getValueAt(selectedRow, 1);
            // JOptionPane.showMessageDialog(Form.this, "selected value :" + id);

            try {

                Cheque cheque = ChequeUtilities.searchCheque(id);

                if (cheque != null) {
                    idText.setText(cheque.getPaymentId());
                    chequeNoText.setText(cheque.getChequeNo());
                    nameText.setText(cheque.getBankName());
                    dateChooser.setDate(cheque.getChequeDate());
                    statusText.setText(cheque.isStatus() + "");
                    amountText.setText(cheque.getChequeAmount() + "");

                } else {
                    JOptionPane.showMessageDialog(ChequeForm.this, "No Such Cheque :" + id);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(ChequeForm.this, "Wrong sql");
            }
        }

        validator.removeWarning(idText);
        validator.removeWarning(chequeNoText);
        validator.removeWarning(nameText);
        validator.removeWarning(statusText);
        validator.removeWarning(amountText);
    }//GEN-LAST:event_jTable1MouseClicked

    private void idTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idTextActionPerformed

        try {
            if (CashForm.paymentIdText.getText().equals(idText.getText())) {
                addButton.setEnabled(true);
                chequeNoText.requestFocus();
            } else {
                addButton.setEnabled(false);
            }
        } catch (NullPointerException nm) {
        }

        if(!ValidationPayment.isValidPaymentId(idText.getText())){
            validator.addWarning(idText);
            idText.setText("");
        }else{
            chequeNoText.requestFocus();
            validator.removeWarning(idText);
        }

    }//GEN-LAST:event_idTextActionPerformed

