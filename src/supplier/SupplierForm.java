    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(SupplierForm.this, "non selected row");
        }
        if (selectedRow != -1) {

            String id = (String) jTable1.getValueAt(selectedRow, 0);
            // JOptionPane.showMessageDialog(Form.this, "selected value :" + id);

            try {

                Supplier supplier = SupplierUtilities.searchSupplier(id);

                if (supplier != null) {
                    idText.setText(supplier.getSupId());
                    nameText.setText(supplier.getSupName());
                    cityText.setText(supplier.getCity());
                    eMailText.setText(supplier.geteMail());
                    comNameText.setText(supplier.getComName());
                    addressText.setText(supplier.getAddress());
                    telephoneText.setText(supplier.getTelephone());

                } else {
                    JOptionPane.showMessageDialog(SupplierForm.this, "No Such Supplier :" + id);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(SupplierForm.this, "Wrong sql");
            }
        }
        
        validator.removeWarning(idText);
        validator.removeWarning(nameText);
        validator.removeWarning(cityText);
        validator.removeWarning(eMailText);
        validator.removeWarning(comNameText);
        validator.removeWarning(addressText);
        validator.removeWarning(telephoneText);
    }//GEN-LAST:event_jTable1MouseClicked

