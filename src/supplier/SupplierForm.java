    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        String id = idText.getText();
        Supplier supplier = new Supplier(id);

        try {

            Object[] options = {"Yes", "No", "Cancel"};
            int option = JOptionPane.showOptionDialog(null, "Are you sure you want to permanently delete this Supplier?", "Delete Supplier", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

            if (option == JOptionPane.YES_OPTION) {

                int rows = SupplierUtilities.deleteSupplier(supplier);
                if (rows > 0) {
                    JOptionPane.showMessageDialog(SupplierForm.this, "Deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(SupplierForm.this, "No such Supplier : " + id);
                }
            }


        } catch (SQLException e) {
            JOptionPane.showMessageDialog(SupplierForm.this, "Wrong SQL");
            //e.printStackTrace();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

